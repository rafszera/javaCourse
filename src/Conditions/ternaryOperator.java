package Conditions;

public class ternaryOperator {
    static void main(String[] args) {

        /*
        * Ternary boilerplate
        * Data type + variable = (condition) ? valueIfTrue : valueIfFalse;
        * '?' its like if
        * ':' its like else
        *
        * */

        short missionNumbers = 11;
        String level = (missionNumbers >= 10) ? "This ninja have more than 10 mission" : "Thins ninja have less than 10 missions";
        System.out.println(level);











































    }
}
